
public class Switch {

	public static void main(String[] args) {
		
		String days="day3";
		switch(days) {
		
		case "day" :
		System.out.println("sunday");
		break;
		
		case "day1" :
		System.out.println("monday");
		break;
		case "day2" :
		System.out.println("friday");
		break;
		case "day3" :
		System.out.println("tusday");
		break;
	}
	}
	}                     