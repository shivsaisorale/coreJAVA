package com.xworkz.diwali;

public class KaveriRiver extends River {

	public boolean sweet;

	public KaveriRiver() {

	}

	public KaveriRiver(boolean sweet) {
		super();
		this.sweet = sweet;
	}

	public boolean isSweet() {
		return sweet;
	}

	public void setSweet(boolean sweet) {
		this.sweet = sweet;
	}

}
